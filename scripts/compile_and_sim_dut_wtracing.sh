# Move to target CORE dir
cd $SRC/circuits/$CORE

################################################################################
################################################################################
## Re-build simulation EXE with VCD tracing enabled
################################################################################
################################################################################
echo "========================================================================="
echo "Re-building HW model with VCD tracing enabled ..."
echo "-------------------------------------------------------------------------"

# Verilate hardware
echo "Verilating hardware ..."
MODEL_DIR=$SRC/circuits/$CORE/$EXP_DATA_PATH/model_vcd
MODEL_DIR=$MODEL_DIR make verilate
echo "-------------------------------------------------------------------------"
echo "Done!"
echo "-------------------------------------------------------------------------"

# Set compilers
echo "Setting compiler/linker flags ..."
export CC="clang"
export CXX="clang++"
export CCC="clang++"
export VLT_CXX="clang++"
export CFLAGS=""
export CXXFLAGS=""
export VLT_CXXFLAGS=""
export LDFLAGS=""

# Print compiler/linker flags
echo "Compiler/Linker Flags:"
echo "CC=$CC"
echo "CXX=$CXX"
echo "CFLAGS=$CFLAGS"
echo "CXXFLAGS=$CXXFLAGS"
echo "VLT_CXXFLAGS=$VLT_CXXFLAGS"
echo "LDFLAGS=$LDFLAGS"
echo "-------------------------------------------------------------------------"
echo "Done!"
echo "-------------------------------------------------------------------------"

# Make a build directory to store AFLGo compiler inputs/outputs
BUILD_DIR=$SRC/circuits/$CORE/$EXP_DATA_PATH/build_vcd
mkdir $BUILD_DIR

# Make a bin directory to store instrumented EXEs
BIN_DIR=$SRC/circuits/$CORE/$EXP_DATA_PATH/bin_vcd
mkdir $BIN_DIR

# Compile SW model of HW
echo "Compiling SW model of HW ..."
MODEL_DIR=$MODEL_DIR BUILD_DIR=$BUILD_DIR BIN_DIR=$BIN_DIR make exe

echo "-------------------------------------------------------------------------"
echo "Done!"

################################################################################
################################################################################
## Generate VCD traces
################################################################################
################################################################################
echo "========================================================================="
echo "Generating VCD traces of AFLGO inputs ..."
echo "-------------------------------------------------------------------------"

# iterate over test files generated by AFLGO
cd $EXP_DATA_PATH/$FUZZER_OUTPUT_DIR
for (( num=1; num <= $NUM_INSTANCES; num++ )); do
    echo "Generating VCD traces for fuzzer instance $num ..."
    cd ${FUZZER_INSTANCE_BASENAME}_${num} && mkdir -p vcd && cd vcd
    for test_file in "../queue/"*; do
        echo "|--Generating VCD trace for: $test_file ..."
        $BIN_DIR/V$CORE $test_file > /dev/null
    done
    cd ..
done
echo "-------------------------------------------------------------------------"
echo "Done!"

################################################################################
################################################################################
## Clean up build
################################################################################
################################################################################
echo "========================================================================="
echo "Cleaning up ..."
rm -rf $BUILD_DIR $BIN_DIR $MODEL_DIR
echo "Done!"
echo "========================================================================="
exit 0
